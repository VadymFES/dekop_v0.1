import type { Metadata } from "next";
import { headers } from "next/headers";
import { getNonce } from "@/app/lib/csp";
import QueryProvider from "@/app/providers/QueryProvider";
import ClientLayout from "./ClientLayout";
import "./global.css";
import { CartProvider } from "./context/CartContext";
import { FavoritesProvider } from "./context/FavoritesContext";
import { SpeedInsights } from "@vercel/speed-insights/next";
import { Analytics } from "@vercel/analytics/next";
import GoogleTagManager from "./components/GoogleTagManager";
import CookieConsent from "./components/CookieConsent";

export const metadata: Metadata = {
 title: "Dekop Furniture Enterprise - меблі для вашого дому",
 description: "Generated by Vadym's Next.js Starter",
 metadataBase: new URL(process.env.NEXT_PUBLIC_SITE_URL || "http://localhost:3000"),
 openGraph: {
  title: "Dekop Furniture Enterprise",
  description: "Меблі для вашого дому",
  url: process.env.NEXT_PUBLIC_SITE_URL || "http://localhost:3000",
  images: [`${process.env.NEXT_PUBLIC_SITE_URL || "http://localhost:3000"}/og-image.png`],
  type: "website",
 },
 icons: {
  icon: [
   { url: "/favicon.png", sizes: "16x16", type: "image/png" },
  ],
 },
};

const GTM_ID = "GTM-TVVGC6PQ";

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const adminPath = process.env.NEXT_PUBLIC_ADMIN_PATH_SECRET ?? 'admin-path-57fyg';
  const headersList = await headers();
  const pathname = headersList.get('x-pathname') || headersList.get('x-invoke-path') || '';
  const isAdminRoute = pathname.includes(adminPath);

  const nonce = isAdminRoute ? '' : await getNonce();

  return (
    <html lang="uk">
      <body>
        {!isAdminRoute && (
          <>
            <script
              nonce={nonce}
              suppressHydrationWarning
              dangerouslySetInnerHTML={{
                __html: `window.dataLayer = window.dataLayer || [];`,
              }}
            />
            <GoogleTagManager gtmId={GTM_ID} nonce={nonce} />
          </>
        )}

        <QueryProvider>
          <CartProvider>
            <FavoritesProvider>
              <ClientLayout isAdminRoute={isAdminRoute}>{children}</ClientLayout>
              {!isAdminRoute && <CookieConsent />}
              {!isAdminRoute && <SpeedInsights />}
              {!isAdminRoute && <Analytics />}
            </FavoritesProvider>
          </CartProvider>
        </QueryProvider>
      </body>
    </html>
  );
}